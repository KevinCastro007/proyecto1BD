    private void BtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAceptarActionPerformed
        if (this.CboTipoEval.getSelectedIndex() != 0 && 
                this.CboInstancia.getSelectedIndex() != 0 &&
                !this.TxtPorcentaje.getText().isEmpty()) {
            try {
                float porcentaje = Float.parseFloat(this.TxtPorcentaje.getText());
                if (porcentaje >= 0 && porcentaje <= 100) {
                    if (this.CboInstancia.getSelectedItem().equals("Fija")) {
                        JDlgInstanciaFija instancia = new JDlgInstanciaFija(null, true);
                        this.setVisible(false);
                        instancia.setLocationRelativeTo(null);
                        instancia.setVisible(true);  
                        evaluacion = new Evaluacion
                        (
                                this.CboTipoEval.getSelectedItem().toString(),
                                porcentaje,
                                this.CboInstancia.getSelectedItem().toString()
                        );
                        evaluacion.agregarInstancias(instancia.getInstancias());
                        JOptionPane.showMessageDialog(rootPane, "Instancias agregadas!");                        
                    }
                    else {
                        evaluacion = new Evaluacion
                        (
                                this.CboTipoEval.getSelectedItem().toString(),
                                porcentaje,
                                this.CboInstancia.getSelectedItem().toString()
                        ); 
                        this.setVisible(false);
                    }                     
                }
                else {
                    JOptionPane.showMessageDialog(rootPane, "Porcentaje inv치lido!");                    
                }
            } 
            catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Porcentaje inv치lido!");                
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Falta informaci칩n!");
        }
    }//GEN-LAST:event_BtnAceptarActionPerformed

