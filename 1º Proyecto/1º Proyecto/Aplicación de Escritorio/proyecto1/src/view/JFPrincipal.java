    private void BtnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEntrarActionPerformed
        String usuario = TxtUsuario.getText();
        String clave = String.valueOf(PwdClave.getPassword());
        try {
            if (Profesor.getInstance().login(usuario, clave)) {
                cargarGrupos(usuario);
                this.TxtUsuario.setText("");
                this.PwdClave.setText("");
                this.jCBGrupos.setEnabled(true);
                this.jBtnDiseñar.setEnabled(true);  
                this.jBtnRegistrar.setEnabled(true); 
                this.jBtnReportar.setEnabled(true);          
            } 
            else {
                JOptionPane.showMessageDialog(rootPane, "Acceso inválido.");
            }
        } catch (SQLException ex) {
            Logger.getLogger(JFPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BtnEntrarActionPerformed


