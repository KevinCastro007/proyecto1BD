    public void cargarGrupos(String pUsuario) throws SQLException {
        this.modelGrupos = new DefaultComboBoxModel();
        this.modelGrupos.addElement("");
        ArrayList<String> grupos = Profesor.getInstance().gruposProfesor(pUsuario);
        for (String grupo : grupos) {
            this.modelGrupos.addElement(grupo);
        }
        this.jCBGrupos.setModel(modelGrupos);
    }    
    
