    private void jBtnDiseñarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnDiseñarActionPerformed
        if (this.jCBGrupos.getSelectedIndex() != 0) {
            JDlgDiseño vistaT = new JDlgDiseño(null, true);
            this.setVisible(false);
            try {
                vistaT.setCodigoGrupo(jCBGrupos.getSelectedItem().toString());
                vistaT.cargarEvaluaciones();
            } catch (SQLException ex) {
                Logger.getLogger(JFPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            vistaT.setLocationRelativeTo(null);
            vistaT.setVisible(true);
            this.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un Grupo!");
        }  
    }//GEN-LAST:event_jBtnDiseñarActionPerformed

    private void jBtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnRegistrarActionPerformed
         if (this.jCBGrupos.getSelectedIndex() != 0) {
            this.setVisible(false);
            JDlgRegistro registro = new JDlgRegistro(null, true);
            try {
                registro.setCodigoGrupo(jCBGrupos.getSelectedItem().toString());
                registro.cargarEstudiantes();
                registro.cargarEvaluaciones();
                registro.inicializarNotas();
            } catch (SQLException ex) {
                Logger.getLogger(JFPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            registro.setLocationRelativeTo(null);
            registro.setVisible(true);
            this.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un Grupo!");
        }
    }//GEN-LAST:event_jBtnRegistrarActionPerformed

