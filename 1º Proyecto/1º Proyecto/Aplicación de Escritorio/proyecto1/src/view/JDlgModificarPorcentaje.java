    public void cargarEvaluaciones() {      
        modelEvaluaciones = new DefaultComboBoxModel();
        this.modelEvaluaciones.addElement("");  
        for (int i = 0; i < evaluaciones.size(); i++) {
            modelEvaluaciones.addElement(evaluaciones.get(i).getTipoEvaluacion());
        }
        this.jCB.setModel(modelEvaluaciones);        
    }
    
