    private void jBtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnModificarActionPerformed
        if (this.jCB.getSelectedIndex() != 0) {
            if (esEvaluacion) {
                for (Evaluacion evaluacion : evaluaciones) {       
                    try {
                        if (evaluacion.getTipoEvaluacion().equals(this.jCB.getSelectedItem().toString())) {
                            float porcentaje = Float.parseFloat(this.jTxtPorcentaje.getText());
                            if (porcentaje >= 0 && porcentaje <= 100) {
                                evaluacion.setPorcentaje(Float.parseFloat(this.jTxtPorcentaje.getText()));
                                this.setVisible(false);
                            }                       
                        } 
                    }
                    catch (Exception e) {
                        JOptionPane.showMessageDialog(rootPane, "Porcentaje inv치lido!"); 
                        System.out.println(e);
                    }
                }
            }
            else {
                 for (InstanciaEvaluacion instancia : instancias) {       
                    try {
                        if (instancia.getDescripcion().equals(this.jCB.getSelectedItem().toString())) {
                            float valor = Float.parseFloat(this.jTxtPorcentaje.getText());
                            if (valor >= 0 && valor <= 100) {
                                instancia.setValor(Float.parseFloat(this.jTxtPorcentaje.getText()));
                                this.setVisible(false);
                            }                       
                        } 
                    }
                    catch (Exception e) {
                        JOptionPane.showMessageDialog(rootPane, "Valor inv치lido!"); 
                        System.out.println(e);
                    }
                }           
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Falta informaci칩n!");
        }
    }//GEN-LAST:event_jBtnModificarActionPerformed

