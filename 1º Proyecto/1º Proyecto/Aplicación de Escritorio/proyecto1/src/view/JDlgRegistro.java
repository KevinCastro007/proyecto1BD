    public void setCodigoGrupo(String pCodigo) throws SQLException {
        this.codigoGrupo = pCodigo;
    }

    public void cargarEstudiantes() throws SQLException {
        modelEstudiantes = new DefaultComboBoxModel();
        this.modelEstudiantes.addElement("");
        ArrayList<String> estudiantes = Miembro.getInstance().miembrosGrupo(codigoGrupo);
        for (String estudiante : estudiantes) {
            this.modelEstudiantes.addElement(estudiante);
        }
        this.jCBEstudiantes.setModel(modelEstudiantes);
    }

    public void cargarEvaluaciones() throws SQLException {
        modelEvaluaciones = new DefaultComboBoxModel();
        this.modelEvaluaciones.addElement("");
        this.evaluaciones = EvaluacionGrupo.getInstance().evaluacionGrupo(codigoGrupo);
        for (int i = 0; i < evaluaciones.size(); i += 4) {
            modelEvaluaciones.addElement(evaluaciones.get(i));
        }
        this.jCBEvaluaciones.setModel(modelEvaluaciones);
    }
    
    public void inicializarNotas() throws SQLException {
        ArrayList<String> miembros = Miembro.getInstance().miembrosGrupo(this.codigoGrupo);
        for (String miembro : miembros) {
            this.evaluaciones = EvaluacionGrupo.getInstance().evaluacionGrupo(codigoGrupo);
            for (int i = 0; i < evaluaciones.size(); i += 4) {                
                EvaluacionGrupo.getInstance().inicializarNotas(miembro, codigoGrupo, evaluaciones.get(i));
            }        
        }        
    }

