    private void jBtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnRegistrarActionPerformed
        if (this.jCBEvaluaciones.getSelectedIndex() != 0 && this.jCBEstudiantes.getSelectedIndex() != 0) {
            for (int i = 0; i < this.evaluaciones.size(); i += 4) {
                if (this.evaluaciones.get(i).equals(this.jCBEvaluaciones.getSelectedItem().toString())) {
                    if (this.evaluaciones.get(i + 2).equals("Fija")) {
                        this.setVisible(false);
                        JDlgRegistroIF registroIF = new JDlgRegistroIF(null, true);
                        try {                            
                            registroIF.setCodigoGrupo(this.codigoGrupo);
                            registroIF.setEvaluacion(this.jCBEvaluaciones.getSelectedItem().toString());
                            registroIF.setEstudiante(this.jCBEstudiantes.getSelectedItem().toString());
                            registroIF.cargarInstancias();
                            registroIF.cargarComboBoxInstancias();
                            registroIF.cargarTablaInstancias();
                        } catch (SQLException ex) {
                            Logger.getLogger(JFPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        registroIF.setLocationRelativeTo(null);
                        registroIF.setVisible(true);                        
                        this.jCBEvaluaciones.setSelectedIndex(0);
                        this.jCBEstudiantes.setSelectedIndex(0);
                        this.setVisible(true);                        
                    } else {
                        this.setVisible(false);
                        JDlgRegistroIV registroIV = new JDlgRegistroIV(null, true);
                        try {
                            registroIV.setCodigoGrupo(this.codigoGrupo);
                            registroIV.setEvaluacion(this.jCBEvaluaciones.getSelectedItem().toString());
                            registroIV.setEstudiante(this.jCBEstudiantes.getSelectedItem().toString());   
                            registroIV.cargarInstancias();     
                            registroIV.cargarTablaInstancias();                    
                        } catch (SQLException ex) {
                            Logger.getLogger(JFPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        registroIV.setLocationRelativeTo(null);
                        registroIV.setVisible(true);            
                        this.jCBEvaluaciones.setSelectedIndex(0);
                        this.jCBEstudiantes.setSelectedIndex(0);
                        this.setVisible(true); 
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Falta informaciÃ³n!");
        }
    }//GEN-LAST:event_jBtnRegistrarActionPerformed

